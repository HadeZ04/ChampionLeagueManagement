export const getLogoByTeamName = (teamName) => {
    if (!teamName) return null;
    // Direct match
    if (TEAM_LOGOS[teamName]) return TEAM_LOGOS[teamName];

    // Case insensitive match
    const lowerName = teamName.toLowerCase();
    const foundKey = Object.keys(TEAM_LOGOS).find(key => key.toLowerCase() === lowerName);
    return foundKey ? TEAM_LOGOS[foundKey] : null;
};
